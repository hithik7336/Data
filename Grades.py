marks = int(input("Enter Marks: "))

if((marks<=70) and (marks>=65)):
    print("Grade Is A1")
    
elif((marks<=65) and (marks>=60)):
    print("Grade Is A2")
    
elif((marks<=60) and (marks>=55)):
    print("Grade Is B1")
        
elif((marks<=55) and (marks>=50)):
    print("Grade Is B2")
        
elif((marks<=50) and (marks>=45)):
    print("Grade Is C1")
    
elif((marks<=45) and (marks>=40)):
    print("Grade Is C2")
    
elif((marks<=40) and (marks>=35)):
    print("Grade Is D")
    
else:
    print("Fail")
        
    
 